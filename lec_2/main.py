# Subtraction
difference = 17 - 2
print("Difference:", difference)

# Multiplication
product = 5 * 7
print("Product:", product)

# Division
quotient = 36 / 3
print("Quotient:", quotient)

# Integer Division
int_quotient = 16 // 3
print("Integer Quotient:", int_quotient)

# Modulo (remainder)
remainder = 17 % 5
